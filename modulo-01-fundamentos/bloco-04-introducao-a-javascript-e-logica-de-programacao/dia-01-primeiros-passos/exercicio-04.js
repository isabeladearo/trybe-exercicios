//04. Faça um programa que, dado um valor definido numa constante, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.

const number = -15;

if (number >= 1) {
    console.log('Positive');
} else if (number <= -1) {
    console.log('Negative');
} else {
    console.log('Zero');
}